           if(dateTemp==value){
            temp.forEach((value,index)=>{
              if(dateTemp==value){
                toAdd = false;
                numbers[index]++
              }
            });
           } 
          }
          if(toAdd){
             temp.push(value)
             numbers.push(1);
           }
        });
